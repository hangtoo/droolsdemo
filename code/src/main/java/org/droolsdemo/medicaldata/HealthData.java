package org.droolsdemo.medicaldata;

public class HealthData {

    private int age;
    private boolean femaleGender;
    private boolean maleGender;
    private float bodyHeight;
    private float bodyWeight;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public boolean isFemaleGender() {
        return femaleGender;
    }

    public void setFemaleGender(boolean femaleGender) {
        this.femaleGender = femaleGender;
    }

    public boolean isMaleGender() {
        return maleGender;
    }

    public void setMaleGender(boolean maleGender) {
        this.maleGender = maleGender;
    }

    public float getBodyHeight() {
        return bodyHeight;
    }

    public void setBodyHeight(float bodyHeight) {
        this.bodyHeight = bodyHeight;
    }

    public float getBodyWeight() {
        return bodyWeight;
    }

    public void setBodyWeight(float bodyWeight) {
        this.bodyWeight = bodyWeight;
    }


}
